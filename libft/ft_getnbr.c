#include "libft.h"

int		ft_getnbr(char *str)
{
	int	res;
	int	sign;

	res = 0;
	sign = 1;
	if (*str == '-')
	{
		sign = -1;
		str++;
	}
	while (*str)
		res = res * 10 + *str++ - '0';
	return (res * sign);
}
